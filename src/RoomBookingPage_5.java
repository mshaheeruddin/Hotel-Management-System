    public ArrayList<String> categRecord()
    {
        ArrayList<String> categ = new ArrayList<>();
        try {
           
            Statement st = conn.createStatement();
            String query = "SELECT Category_name from ROOM_CATEGORY";
            ResultSet rs = st.executeQuery(query);
            
            while(rs.next())
            {
                categ.add(rs.getString("Category_name"));
            }
             
            st.close();
            
        } catch (SQLException ex) {
            Logger.getLogger(RoomBookingPage_5.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        return categ;
    }
    
    public void showCategory() 
    {
        ArrayList<String> list = categRecord();
        list.add("Gold");
        Object[] categList = list.toArray();
        DefaultComboBoxModel cb = new DefaultComboBoxModel(categList);
        CategoryOption.setModel(cb);
        
    }
    
