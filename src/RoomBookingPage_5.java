        // TODO add your handling code here:
            
            roomType = (CategoryOption.getSelectedItem()).toString();
            noOfPerson = Integer.parseInt(jTextField2.getText());
            checkIn = jDateChooser1.getDate();
            checkOut = jDateChooser2.getDate();
            
            SimpleDateFormat df = new SimpleDateFormat("YYYY-MM-DD");
            Date currentDate = new Date();
            df.format(currentDate);
            
            
            
        try {
            String query = "SELECT quantity FROM ROOM_CATEGORY WHERE Category_name=?";
            PreparedStatement stp = conn.prepareStatement(query);
            
            stp.setString(1,roomType);
            
            ResultSet rs = stp.executeQuery();
            
            rs.next();
        
            noOfRoomsAvail = rs.getInt("quantity");
            
                       
        } catch (SQLException ex) {
            Logger.getLogger(RoomBookingPage_5.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        if(noOfRoomsAvail == 0)
        {
            JOptionPane.showMessageDialog(null, "Sorry! No room available of this type.", null, JOptionPane.ERROR_MESSAGE);
        }
        else if(noOfPerson == 0)
        {
            JOptionPane.showMessageDialog(null, "Enter Valid Number of Persons", null, JOptionPane.ERROR_MESSAGE);
        }
        else if(checkIn.compareTo(currentDate) < 0)
        {
            JOptionPane.showMessageDialog(null, "Enter valid date", null, JOptionPane.ERROR_MESSAGE);
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Booking successful");                                                          //GET BOOKING ID FROM DATABSE INCREMENTED
        }
        
